        		<p>一、募資項目申請資格</p>

<p>(一)提案者必須是具備完全行為能力的自然人、或合法登記的法人或團體。若提案者為未成年人，應由其法定代理人閱讀、暸解、並同意群眾募資平台 Wefund 提案契約書(「本合約」)之所有約定內容及其修改後之內容，始得開始使用或繼續使用本服務。</p>
 
<p>(二) 提案者應完成註冊程序，包括填寫會員資料、以及提供註冊流程中所要求之相關文件或資料。提案者欲使用本服務之所有功能者，並應依本服務當時所訂之方式完成會員認證。</p>
 
<p>(三) 提案者應擔保其所提供的所有資料，均為正確且即時的資料；如提案者所提供的資料事後有變更，提案者應即聯絡本系統之管理員，請管理員協助更新資料。如提案者未即時提供資料、未按指定方式提供資料、所提供之資料不正確或與事實不符、或未即時更新資料，Wefund 得不經事先通知，隨時拒絕、或暫停對該提案者提供本服務之全部或一部。</p>


<p>二、禁止募資事項

以資金、股份或有價證券作為回饋。
具博弈性質之競賽、抽獎，及永久的會員資格。
鼓吹暴力、仇恨或歧視特定及不特定族群之攻擊性內容。
對身體產生生理傷害和依賴性之菸草製品、管制藥品及其用具。
保健食品。
政治獻金。
散播色情內容。
具有殺傷力之武器、組裝方式及組成零件。
任何宣稱使用於診斷、治療、減輕、直接預防人類疾病、調節生育，或足以影響人類身體結構及機能之藥物，包含所有藥品之原料、製劑及達成其主要功能之儀器、器械、用具、物質、軟體、體外試劑及其相關物品。</p>
其他經平台判斷之不當內容。
 

<p>三、群眾募資平台服務</p>

<p>(一) Wefund 提供網路群眾募資平台服務，供提案者自行刊載內容進行募資。
Wefund 項目募資期間，Wefund 提供平台供贊助內容刊載、代收贊助款項並紀錄贊助者所選回饋項目與金額。</p>

 

<p>(二)提案者應完整刊載包含：

(1)項目說明影片

(2)項目募資金額目標

(3)項目說明文案

(4)提供贊助者之回饋項目</p>

 

<p>(三 )Wefund 上所刊載之物件、說明內容、及相關訊息，包括所刊載之多媒體檔案，均係由提案者自行提供、上載、及發布，並由本服務系統刊載於網站，提案者應擔保其內容之真實性、合法性、即時性等，Wefund 就刊載內容之真實性、合法性、即時性等並無審查之義務，亦不負任何明示或默示之承諾或擔保。提案者所刊載之物件、說明內容、或相關訊息等，如有違反法令、違背公序良俗、侵害第三人權益、或有違反本合約之虞之情形，Wefund 得不經事先通知，直接加以移除、使之無法被存取或被閱讀、或採取其他限制性措施。</p>

 

<p>(四)經由 Wefund 所進行之募資與回饋項目，應由提案者自行負責回饋之磋商及履行，Wefund 認為必要時，得就提案者提供之資料、提案者所刊載之物件及相關訊息、以及與募資相關之事項等，要求提案者就其所涉及之疑義或爭議，即時提出說明及有關資料。</p>

 

<p>(五)如贊助者於募資項目進行期間要求退款或變更贊助內容，Wefund 得變更其贊助項目與內容，提案者不得異議。如贊助者於募資項目成功結束後方要求退款或變更贊助內容，提案者應自行衡量允可與否、並自行處理退款與後續相關事宜，概與 Wefund 無涉。</p>

 

<p>(六)若回饋履行進度延誤或回饋內容有所變更，提案者應即時使用「進度報告」功能將資訊公開於 Wefund 項目頁面。贊助者若因上述變更要求變更贊助或退款，提案者不得拒絕或扣除部分費用。</p>

 

<p>四、手續費用

(一)刊載於 Wefund 之募資案件到期後，若達到或超過提案者設定之募資金額，Wefund 將自總金額中收取 5 % 作為服務費。

 

(二)如募資案件到期後，未達到提案者設定之募資金額，Wefund 將不收取任何手續費，並於次月 10 日(非工作日則順延)將募資金額退還予該案件贊助者，惟每筆經由超商付款額外產生之 35 元將無法退還。

 

(三)募資項目成功完成後，Wefund 將於收到匯款資料後的 7～14 個工作天內出款(非工作日則順延)，以匯款方式交付扣除服務費與匯費之募資款項予提案者。</p>

 

<p>五、內容授權與智財權確保

(一)提案者上載、傳送或提供物件資料或其他相關資料至 Wefund 時，視為提案者已授權 Wefund 得利用、儲存及刊載該等資料，以供特定或不特定之使用者搜尋及瀏覽，並視為提案者已授權Wefund得經由平面或電子形式，重製、散布、公開播送、公開上映、改作、編輯、公開發行、公開發表、或公開傳輸該等資料。

 

(二)提案者上載、傳送或提供物件資料或其他相關資料至 Wefund 時，應擔保其有上載、傳送或提供該等資料之權利，若該等資料涉有第三人之智慧財產權，提案者保證已取得合法使用之權利，並已取得於台灣地區(含台、澎、金、馬等外島地區)公開發表、公開播送及公開傳輸等權利。若因提案者提供之內容致使有相關之智慧財產權和法律問題時，提案者須自行負擔責任，概與 Wefund 無涉。</p>

 

<p>六、租用期間專屬授權

除 Wefund 書面同意者外，提案者不得提供第三人為相同或近似內容之展示。</p>

 

<p>七、損害賠償違約金 

乙方違反第一、二、四或第五條條文時，應以 Wefund 所受損害(含律師費用)或提案者所受利益，兩者取其高，作為給付 Wefund 之損害賠償金。</p> 



