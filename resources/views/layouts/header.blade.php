（二）送達：本合約得以電子郵件為送達方式。

（三）契約解釋、補充協議及疑義解釋：本合約條文如有未盡事宜，經雙方同意得以附件補充之，如有疑義應由雙方本友善互助之原則協商處理。

（四）合意準據法與管轄：本合約以中華民國為準據法，並同意以台灣台北地方法院為第一審管轄法院。

（五）本合約以電子文件形式行之，或勾選「同意提案契約書」時，即視為已閱讀、暸解、並同意以上約定條款的所有內容。</p>





             
