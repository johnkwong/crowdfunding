提案者必須是 WeFund 的會員。
提案者必須是具備完全行為能力的自然人、合法登記的法人或團體。若提案者為未成年人，應由其法定代理人閱讀、暸解，才可進行提案流程。提案前，應完成註冊程序，並應依本平台當時所訂之方式完成會員認證。
