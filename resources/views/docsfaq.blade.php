從提交到項目人員的回覆，需要 2 – 5 個工作天。
群眾募資的本意在於「去中間化」，亦即讓市場自己決定什麼計畫應當成真，WeFund 將僅就提案的型式而非內容進行審核。除非您的案件有違法之疑慮、或涉及具高度爭議性之內容，否則 WeFund 將不會禁止項目的刊登
