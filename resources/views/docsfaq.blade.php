 <legend>履行回饋</legend>

 <div class="thumbnail">
	<h4><b>平台上的贊助到底是什麼行為？</b></h4>
在 WeFund 上發起的群眾募資案件，本質上不同於一般的「贈與」或「交易」。贊助者給予提案者資金；提案者在完成提案後給予贊助者回饋物，WeFund 提供項目刊載與金流服務，當募資項目成功時，平台會向提案者收取募資金額的 8% 作為手續費；當募資項目失敗時，平台會退還募資金額予贊助者。除刊載與金流外，項目回饋之履行與遞交及稅捐稽徵事宜(如需開立發票或收據)則為提案者之責任。
為達監管之效，WeFund 認為必要時得就提案者提供之資料、刊載之物件及相關訊息、以及與募資相關之事項等，要求提案者就其所涉及之疑義或爭議，即時提出說明及有關資料。如有違反法令、違背公序良俗、侵害第三人權益之情形，WeFund 得直接移除其內容，並提供相關資料配合法律調查。
 </div>

<div class="thumbnail">	
	<h4><b>項目募資成功了，我什麼時候可以拿到錢？</b></h4>
項目募資成功後的 10 個工作天後，會收到 WeFund 傳送的結案報告，若是贊助者在時限內回傳匯款帳號資料，WeFund 會在隔週的星期五匯出款項。若是項目遭遇到不可抗力之因素（客服、退款等），可能會延遲出款日期。
 </div>

<div class="thumbnail">	
	<h4><b>履行回饋的重要性是？</b></h4>
在群眾募資平台上，履行回饋是提案者的責任。WeFund 提供的各項服務旨在更有效便捷的媒合提案者與贊助者，我們會善盡平台的責任，但並不能擔保提案者能百分之百的履行其計畫內容。如有發生糾紛爭議，我們將依循[提案者合約]與[網站使用條約]處理。
 </div>

<div class="thumbnail">	
	<h4><b>項目會不會有詐騙的風險？</b></h4>
我們不能保證每一個項目都能完美達成承諾，但會盡一切可能保障贊助者的權益。WeFund 要求每一個提案者都必須公開自己的身份，也會盡力進行查證之責。提案過程中提案者必須同意[提案者合約]，如有法律爭議，會依照合約規範進行處理。
 </div>

<div class="thumbnail">	
	<h4><b>提案者的身分是經過查證的嗎？</b></h4>
我們會善盡最大力量進行申請者身分的查證，WeFund 平台在審查時會要求項目申請者提交身分證明的資料：
個人申請者：身分證號、護照編號、駕照或居留證等
團體申請者：機關統一編號、登記證明或其他文件
 </div>

<div class="thumbnail">	
	<h4><b>項目募資成功了，但是我一直沒有收到回饋</b></h4>	
請確認項目的[進度報告]和您登記的信箱，檢查是否有提案者發佈的訊息；若無，則可寄信至提案者信箱反應。若仍無回應，則請將相關情形寄至：WeFund@wefund.com，我們會要求提案者盡速說明。
 </div>

<div class="thumbnail">	
	<h4><b>什麼是「風險與承諾」？</b></h4>
我們明白，每個提案者都非常希望能順利履行回饋，但天有不測風雲，為了保障雙方的權益，我們要求提案者必須在項目內容中標明項目進行可能遭遇的挑戰與困難，並且清楚說明如果在募資成功後卻無法或延期履行回饋的處理方式，以避免爭議的產生。除此之外，你也可以公開你的支出帳目，讓贊助者知道你得到的每一分錢都花在該用的地方。
