如果募資過程中間，接受他人檢舉，或是發現項目有法律或其他方面的疑慮，為保障所有會員的權益，WeFund 會暫時中止您的項目。
項目經理在中止項目之前，必定會先與提案者聯繫，了解當前狀況後會發信件通知所有贊助者作為公告。
